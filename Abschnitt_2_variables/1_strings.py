#häufige string Methoden
first_name = "vOlKeR"
print(f"Hallo {first_name.title()}") # title() erzeugt 1. Buchstabe groß, alles weitere klein
print(f"Hallo {first_name.upper()}") # upper() erzeugt alle Buchstaben groß
print(f"Hallo {first_name.lower()}") # lower() erzeugt alle Buchstaben klein